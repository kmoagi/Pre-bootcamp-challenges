      function cToF(celsius) 
      {
        var cTemp = celsius;
        var cToFahr = cTemp * 9 / 5 + 32;
        var message = cTemp  + cToFahr  ;
          console.log(message);
      }
      
      function fToC(fahrenheit) 
      {
        var fTemp = fahrenheit;
        var fToCel = (fTemp - 32) * 5 / 9;
        var message = fTemp + fToCel ;
          console.log(message);
      } 
      cToF(5);
      fToC(10);


      
